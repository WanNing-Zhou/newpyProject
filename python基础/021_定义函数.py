# 定义函数,能够完场打印信息功能
def fun():
    print('欢迎来到红浪漫')


# 定义完函数后, 函数不是自动执行的, 需要调用它才可以
fun()  # 欢迎来到红浪漫


# 定义一个函数,进行两个数的加法运算
def add(a, b):
    c = a + b
    print(c)


# 调用函数
add(1, 2)






